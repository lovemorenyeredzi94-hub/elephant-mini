/**
 * Arena Text Effect
 */

const mumaker = require('mumaker');

module.exports = {
    pattern: "arena",
    alias: [],
    desc: "Create arena text effect",
    category: "textmaker",
    react: "⚔️",
    filename: __filename,
    use: ".arena <text>",
    
    execute: async (conn, message, m, { from, isGroup, reply }) => {
        try {
            const args = m.args || [];
            const text = args.join(' ');
            
            if (!text) {
                return await reply('Please provide text to generate\nExample: .arena Nick');
            }
            
            const result = await mumaker.ephoto('https://en.ephoto360.com/create-cover-arena-of-valor-by-mastering-360.html', text);
            
            if (!result || !result.image) {
                throw new Error('No image URL received from the API');
            }
            
            const botName = process.env.BOT_NAME || 'ELEPHANT-MD';
            await conn.sendMessage(from, {
                image: { url: result.image },
                caption: `GENERATED BY ${botName}`
            }, { quoted: message });
            
            if (module.exports.react) {
                await conn.sendMessage(from, { react: { text: module.exports.react, key: message.key } });
            }
        } catch (error) {
            console.error('[arena] error:', error);
            await reply(`Error: ${error.message}`);
        }
    }
};